<p>
  Dizi, kendi zamanlarını aile hayatı ve boyutlar arası yolculuk arasında bölüştürmüş alkolik
  bilim insanı Rick ve onun torunu Morty'nin maceralarını anlatır. Dizide Chris Parnell,
  Spencer Grammer ve Sarah Chalke'in sesi başroldeki karakterleri seslendirken diğer
  karakterleri Roiland seslendirir. Dizinin ilk halleri Roiland tarafından Channel 101 film
  festivali için hazırlanmıştır. Adult Swim, televizyon şovu fikirleri için Harmon ile
  görüştü; o ve Roiland iki karaktere dayalı televizyon programını hazırlamaya başladılar.
</p>
<p>

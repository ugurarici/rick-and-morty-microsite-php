<h2>Felsefesi</h2>
<p>
  Rick and Morty "Nihilist bir bakışın etrafına sarılmış hiç bitmeyen bir osuruk şakası"
  olarak betimlenir. Seri, evrenin büyüklüğüne kıyasla insan varoluşunun önemsizliği üzerinde
  durur. Serinin karakterleri kozmik korku ve varoluşsal dehşetle, ya bilimin sihre göre
  faydasını öne sürerek ya da cahil bir mutluluk içinde bir yaşam seçerek başa çıkmaya
  çalışır. Ancak Geeks Notes'ten Joachim Heijndermans'a göre hiçbiri evrenin absürt ve kaotik
  doğasıyla başa çıkamıyor gibi görünüyor.
</p>
<p>
  Harmon, Rick'i ne yapması gerektiğinin söylenmesinden hoşlanmayan kendi odaklı bir anarşist
  olarak tanımlar. The Humanist dergisinden Matthew Bulger, dizinin yaratıcılarının bir amaç
  duygusu bulmak ve daha iyi bir yaşam sürmek için insan ilişkilerine odaklanmamız ve
  zihnimizi cevapsız sorularla meşgul etmememiz gerektiği mesajını iletmeye çalıştıklarını
  belirtiyor. The New Republic'ten Eric Armstrong ise Morty'nin çoğunlukla Rick'in çılgın
  planlarına tepki vermek için orada olduğundan izleyiciyi temsil ettiğini belirtiyor. Eric'e
  göre karakter, dedesi ile olan boyutlararası maceraları sırasında keşfettiği gerçeklerle
  değişimler geçirir ancak Morty bu değişimler karşısında depresyona girmek yerine, kendi
  hayatına verdiği değeri güçlendirmek için için bu gerçekleri kabul eder.
</p>
